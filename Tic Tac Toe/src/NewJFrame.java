    int x_o=0;
    boolean winner=false;
    public ActionListener createAction(JButton bt){
        ActionListener a1=new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if(bt.getText().equals("")&&winner==false){
                        if(x_o%2==0){
                           bt.setText("X");
                           bt.setForeground(Color.RED);
                          jLabel1.setText("It's Player O Turn");
                           getWinner();
                        }
                         else{
                           bt.setText("O");
                           bt.setForeground(Color.BLUE);   
                          jLabel1.setText("It's Player X Turn");
                           getWinner();
                        }
                        x_o++;
                 }
            }
        };
        return a1;
    }
    
    public void addAction(){
        Component[] comps=jPanel1.getComponents();
          
        for(Component comp:comps){
            if(comp instanceof JButton){
                 JButton bt=(JButton)(comp);
                bt.addActionListener(createAction(bt));
            }
        }
    }
    
    public void chechWinner(JButton b1,JButton b2,JButton b3){
      if(!b1.getText().equals("")&&b1.getText().equals(b2.getText())&&b1.getText().equals(b3.getText())){
          b1.setBackground(Color.BLACK);
          b2.setBackground(Color.BLACK);
          b3.setBackground(Color.BLACK);

          b1.setForeground(Color.WHITE);
          b2.setForeground(Color.WHITE);
          b3.setForeground(Color.WHITE);
          
          String win="Player  "+b1.getText()+"  Is The Winner ";
          jLabel1.setText(win);
          if(b1.getText().equals("O"))
              jPanel2.setBackground(Color.BLUE);
          else
              jPanel2.setBackground(Color.RED);
            winner=true;
       }
     }
    
    public void getWinner(){
           chechWinner(jButton1, jButton2, jButton3);
           chechWinner(jButton4, jButton5, jButton6);
           chechWinner(jButton8, jButton9, jButton10);
           chechWinner(jButton3, jButton4, jButton8);
           chechWinner(jButton1, jButton5, jButton9);
           chechWinner(jButton1, jButton5, jButton9);
           chechWinner(jButton2, jButton6, jButton10);
           chechWinner(jButton3, jButton5, jButton10);
           chechWinner(jButton2, jButton5, jButton8);

           if(noWinner()==9&&winner==false){
                 jLabel1.setText("There Is No Winner ");
                 jPanel2.setBackground(Color.BLACK);
           }
    }
   
