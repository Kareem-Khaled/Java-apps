    public int checkData() {
        if (jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("")
                || String.valueOf(jPasswordField2.getPassword()).equals("") || String.valueOf(jPasswordField3.getPassword()).equals("")) {
            return 1;
        } else if (!String.valueOf(jPasswordField2.getPassword()).equals(String.valueOf(jPasswordField3.getPassword()))) {
            return 2;
        } else {
            return 0;
        }
    }
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Connection con = myConnection.getConnection();
        PreparedStatement ps;
        int check = checkData();
        if (check == 1) {
            JLabel label = new JLabel("One or more field are empty !");
            label.setFont(new Font("Arial", Font.PLAIN, 15));
            JOptionPane.showMessageDialog(null, label, "Take Care", JOptionPane.INFORMATION_MESSAGE);
        } else if (check == 2) {
            JLabel label = new JLabel("The password are wrong");
            label.setFont(new Font("Arial", Font.PLAIN, 15));
            JOptionPane.showMessageDialog(null, label, "Take Care", JOptionPane.INFORMATION_MESSAGE);
        } else {

            try {
                ps = con.prepareStatement("INSERT INTO `newuser` (`firstName`,`lastName`,`userName`,`Password`) VALUES (?,?,?,?);");
                ps.setString(1, jTextField2.getText());
                ps.setString(2, jTextField3.getText());
                ps.setString(3, jTextField4.getText());
                ps.setString(4, String.valueOf(jPasswordField2.getPassword()));
                if (ps.executeUpdate() != 0) {
                    JLabel label = new JLabel("Account Created Successfully !");
                    label.setFont(new Font("Arial", Font.PLAIN, 15));
                    JOptionPane.showMessageDialog(null, label, "Done", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JLabel label = new JLabel("Something Wrong !");
                    label.setFont(new Font("Arial", Font.PLAIN, 15));
                    JOptionPane.showMessageDialog(null, label, "Error", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(SignupForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

